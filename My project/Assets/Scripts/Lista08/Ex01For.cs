using UnityEngine;

public class Ex01For : MonoBehaviour
{
    void Start()
    {
        /*1. Contador simples no Console
Crie um script que imprima no Console os números de 1 a 10 usando for.
Desafio extra: mostre apenas os números pares.
         */
        for (int x = 0; x <= 10; x++)
        {
            print(x);
        }
    }
}
